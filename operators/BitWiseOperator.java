package operators;

public class BitWiseOperator {

	public static void main(String[] args) {
		
		int a=5;
		int b=6;
		int and=a&b;
		int or=a|b;
		int rightshift=a>>1;
		int leftshift =a<<1;
		System.out.println(and);
		System.out.println(or);
		System.out.println(rightshift);
		System.out.println(leftshift);
		
	}

}
