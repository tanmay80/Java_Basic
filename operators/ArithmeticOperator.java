package operators;

public class ArithmeticOperator {

	public static void main(String[] args) {
	
		int a=23;
		int b=45;
		
		int sum=a+b;
		int mul=a*b;
		int div=a/b; 
		int mod=a%b;
		int sub=a-b;
		System.out.println(sum);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
		System.out.println(sub);
	}

}
